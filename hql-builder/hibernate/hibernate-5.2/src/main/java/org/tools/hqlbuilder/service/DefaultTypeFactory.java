package org.tools.hqlbuilder.service;

import org.hibernate.type.Type;

@SuppressWarnings("rawtypes")
public class DefaultTypeFactory {
    private static org.hibernate.type.TypeResolver typeResolver = new org.hibernate.type.TypeResolver();

    public static Type resolve(String name) {
        return typeResolver.basic(name);
    }

    public static Type resolve(Class clas) {
        return typeResolver.basic(clas.getName());
    }

    public static Type basic(String name) {
        return typeResolver.basic(name);
    }
}
